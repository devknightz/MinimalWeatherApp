package you.devknights.minimalweather.network.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.List;

import you.devknights.minimalweather.database.entity.WeatherEntity;

public class WeatherResponse {

    @SerializedName("coord")
    @Expose
    private Coord coord;
    @SerializedName("weather")
    @Expose
    private List<Weather> weather = null;
    @SerializedName("base")
    @Expose
    private String base;
    @SerializedName("main")
    @Expose
    private Main main;
    @SerializedName("visibility")
    @Expose
    private int visibility;
    @SerializedName("wind")
    @Expose
    private Wind wind;
    @SerializedName("clouds")
    @Expose
    private Clouds clouds;
    @SerializedName("dt")
    @Expose
    private int dt;
    @SerializedName("sys")
    @Expose
    private Sys sys;
    @SerializedName("id")
    @Expose
    private int id;
    @SerializedName("name")
    @Expose
    private String name;
    @SerializedName("cod")
    @Expose
    private int cod;

    public Coord getCoord() {
        return coord;
    }

    public void setCoord(Coord coord) {
        this.coord = coord;
    }

    public List<Weather> getWeather() {
        return weather;
    }

    public void setWeather(List<Weather> weather) {
        this.weather = weather;
    }

    public String getBase() {
        return base;
    }

    public void setBase(String base) {
        this.base = base;
    }

    public Main getMain() {
        return main;
    }

    public void setMain(Main main) {
        this.main = main;
    }

    public int getVisibility() {
        return visibility;
    }

    public void setVisibility(int visibility) {
        this.visibility = visibility;
    }

    public Wind getWind() {
        return wind;
    }

    public void setWind(Wind wind) {
        this.wind = wind;
    }

    public Clouds getClouds() {
        return clouds;
    }

    public void setClouds(Clouds clouds) {
        this.clouds = clouds;
    }

    public int getDt() {
        return dt;
    }

    public void setDt(int dt) {
        this.dt = dt;
    }

    public Sys getSys() {
        return sys;
    }

    public void setSys(Sys sys) {
        this.sys = sys;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }



    public you.devknights.minimalweather.model.Weather buildWeather() {
        WeatherEntity weatherEntity = new WeatherEntity();

        weatherEntity.setPlaceId(id);
        weatherEntity.setPlaceName(name);
        weatherEntity.setPlaceLat(coord.getLat());
        weatherEntity.setPlaceLon(coord.getLon());

        if (getWeather() != null && getWeather().size() > 0) {
            Weather weather = getWeather().get(0);

            weatherEntity.setWeatherId(weather.getId());
            weatherEntity.setWeatherMain(weather.getMain());
            weatherEntity.setWeatherDescription(weather.getDescription());
            weatherEntity.setWeatherIcon(weather.getIcon());
        }

        weatherEntity.setTemperature(main.getPressure());
        weatherEntity.setPressure(main.getPressure());
        weatherEntity.setHumidity(main.getHumidity());
        weatherEntity.setWindSpeed((float) wind.getSpeed());
        weatherEntity.setSunriseTime(sys.getSunrise());
        weatherEntity.setSunsetTime(sys.getSunset());

        return weatherEntity;

    }

}
